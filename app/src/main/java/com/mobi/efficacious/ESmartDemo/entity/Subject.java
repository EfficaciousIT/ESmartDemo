package com.mobi.efficacious.ESmartDemo.entity;

public class Subject
{
    private String Subject_id;
    private String Subject_name;
private String Subject_url;
    private String Subject_Marks;
    public Subject()
    {

    }

    public String getSubject_id() {
        return Subject_id;
    }

    public void setSubject_id(String subject_id) {
        Subject_id = subject_id;
    }

    public String getSubject_name() {
        return Subject_name;
    }

    public void setSubject_name(String subject_name) {
        Subject_name = subject_name;
    }

    public String getSubject_url() {
        return Subject_url;
    }

    public void setSubject_url(String subject_url) {
        Subject_url = subject_url;
    }

    public String getSubject_Marks() {
        return Subject_Marks;
    }

    public void setSubject_Marks(String subject_Marks) {
        Subject_Marks = subject_Marks;
    }
}
